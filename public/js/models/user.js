function User(id_user,full_name,gender,birthday,email,phone,login,password, confirm_password,id_avatar,avatar_src){
		this.id_user = id_user;
		this.full_name = full_name;
		this.gender = gender;
		this.birthday = birthday;
		this.email = email;
		this.phone = phone;
		this.login = login;
		this.password = password;
		this.confirm_password = confirm_password;
		this.id_avatar = id_avatar;
		this.avatar_src = avatar_src;
}